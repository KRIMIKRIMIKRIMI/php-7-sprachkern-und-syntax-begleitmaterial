    <h1>Größer und kleiner bei Strings</h1>
    <p>
        'hallo' ist nicht größer als 'hallo':
        <?php echo 'hallo' > 'hallo'; ?>
    </p>
    <p>
        'hallo' ist größer oder gleich 'hallo':
        <?php echo 'hallo' >= 'hallo'; ?>
    </p>
    <p>
        'Hallo' ist kleiner als 'hallo':
        <?php echo 'Hallo' < 'hallo'; ?>
    </p>
    <p>
        'hi' ist größer als 'hallo':
        <?php echo 'hi' > 'hallo'; ?>
    </p>
